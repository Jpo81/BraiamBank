    public void listar() {
        Vector cabecalho = new Vector();
        try {
            cabecalho.addElement("RM");
            cabecalho.addElement("Nome");
            cabecalho.addElement("Email");

            ConProfessor conProfessor = new ConProfessor();
            if (conProfessor.listar() == null) {
                JOptionPane.showMessageDialog(rootPane, "Ocorreu uma falha durante a exibição dos dados, tente novamente mais tarde");
            }

            jTable1.setModel(new DefaultTableModel(conProfessor.listar(), cabecalho));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu uma falha durante a exibição dos dados, tente novamente mais tarde");

        }
    }

