    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String rmStr = txtRMProfessor.getText().trim();

            int rm;
            try {
                rm = Integer.parseInt(rmStr);
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(rootPane, "O campo RM deve ser um numero valido!");
                return;
            }

            if (rmStr.length() != 5) {
                JOptionPane.showMessageDialog(rootPane, "RM inválido! Deve ter exatamente 5 dígitos.");
                return;
            }

            ConProfessor conProfessor = new ConProfessor();

            Professor professor = conProfessor.pesquisarPorRM(rm);

            if (professor.getNome_Professor() != null) {
                txtNome.setText(professor.getNome_Professor());
                txtEmail.setText(professor.getEMAIL_Professor());
                txtSenha.setText(professor.getSENHA_Professor());
            } else {
                JOptionPane.showMessageDialog(rootPane, "Professor não encontrado");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro: " + ex);
            txtRMProfessor.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {

            int op = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente editar?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (op == JOptionPane.YES_OPTION) {
                /*Pegando os valores dos Text Fields*/
                String rmStr = txtRMProfessor.getText().trim();
                String nome = txtNome.getText().trim();
                String email = txtEmail.getText().trim();
                String senha = txtSenha.getText().trim();

                /*Validação deve ser posta nessa área*/
                if (rmStr.isEmpty() || nome.isEmpty() || email.isEmpty() || senha.isEmpty()
                        || rmStr.equals("RM do Professor") || nome.equals("Nome do Professor")
                        || email.equals("Email do Professor") || senha.equals("Senha do Professor")) {
                    JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos!");
                    return;
                } else if (!rmStr.matches("\\d+")) {
                    JOptionPane.showMessageDialog(rootPane, "O RM deve conter apenas números!");
                    return;
                } else if (rmStr.length() != 5) {
                    JOptionPane.showMessageDialog(rootPane, "RM inválido! Deve ter exatamente 5 dígitos.");
                    return;
                } else if (nome.length() > 30) {
                    JOptionPane.showMessageDialog(rootPane, "O nome deve ter no máximo 30 caracteres");
                    return;
                } else if (!email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
                    JOptionPane.showMessageDialog(rootPane, "Email inválido!");
                    return;
                } else if (email.length() > 30) {
                    JOptionPane.showMessageDialog(rootPane, "O Email deve ter no maximo 30 caracteres");
                    return;
                } else if (!senha.matches("[0-9a-zA-Z$*&@#]{8,32}")) {
                    JOptionPane.showMessageDialog(rootPane, "A senha deve ter entre 8 e 32 caracteres e ao menos 1 caracter especial ($*&@#)!");
                    return;
                }

                int rm;
                try {
                    rm = Integer.parseInt(rmStr);
                } catch (NumberFormatException nfe) {
                    JOptionPane.showMessageDialog(rootPane, "O campo RM deve ser um numero valido!");
                    return;
                }

                Professor professor = new Professor();
                professor.setRM_Professor(rm);
                professor.setNome_Professor(nome);
                professor.setEMAIL_Professor(email);
                professor.setSENHA_Professor(senha);

                ConProfessor conProfessor = new ConProfessor();
                boolean confirm = conProfessor.editar(professor);
                if (confirm) {
                    JOptionPane.showMessageDialog(rootPane, "Edição feita com Sucesso!!");
                    listar();
                }

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            String rmStr = txtRMProfessor.getText().trim();

            if (rmStr.isEmpty() || rmStr.equals("RM do Professor")) {
                JOptionPane.showMessageDialog(rootPane, "Por favor, digite o RM do professor!");
                return;
            }

            int rm;
            try {
                rm = Integer.parseInt(rmStr);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(rootPane, "O RM deve ser um número válido!");
                return;
            }

            if (rmStr.length() != 5) {
                JOptionPane.showMessageDialog(rootPane, "RM inválido! Deve ter exatamente 5 dígitos.");
                return;
            }

            int op = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir ?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (op == JOptionPane.YES_OPTION) {
                ConProfessor conProfessor = new ConProfessor();

                /*Pega o rm do professor pesquisado, e usa para excluir*/
                conProfessor.excluir(rm);

                /*Mensagem de sucesso*/
                JOptionPane.showMessageDialog(rootPane, "Registro atualizado com sucesso!!!");
                listar();
            }
        } catch (Exception ex) {
            /*Mensagem de erro*/
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro:" + ex);
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

